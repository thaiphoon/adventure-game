public class Treasure{
    /*
     *      Variables
     *          gold int private
     *      Methods
     *          getGold public - no arguments
     */

    private int gold;

    public Treasure(int gold){
        this.gold = gold;
    }

    public int getGold(){
        return gold;
    }
    public void setGold(int value){ this.gold = value; }


}
